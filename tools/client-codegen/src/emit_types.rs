//! Emit Rust type definitions from parsed OpenAPI schemas.

use crate::spec::{Field, ParsedSpec, TypeDef};

/// Full header for the first (or only) types file — includes pagination wrappers.
pub fn header(parsed: &ParsedSpec) -> String {
    let mut out = header_imports(parsed);
    out.push_str(PAGINATION_TYPES);
    out
}

/// Minimal header for continuation types files — imports + crate re-imports.
pub fn header_imports(parsed: &ParsedSpec) -> String {
    let mut out = String::with_capacity(256);
    out.push_str("//! Type definitions for the ");
    out.push_str(&parsed.service_title);
    out.push_str(" client.\n//!\n//! Auto-generated by client-codegen — do not edit.\n\n");
    out.push_str("#![allow(unused_imports)]\n\n");
    out.push_str("use serde::{Deserialize, Serialize};\n");
    out.push_str("use crate::*;\n\n");
    out
}

/// Emit all types as a single string (used when total is under 500 lines).
pub fn emit(parsed: &ParsedSpec) -> String {
    let mut out = header(parsed);
    for td in &parsed.types {
        match td {
            TypeDef::Struct { name, fields, doc } => {
                emit_struct(&mut out, name, fields, doc.as_deref());
            }
            TypeDef::Enum {
                name,
                variants,
                doc,
            } => {
                emit_enum(&mut out, name, variants, doc.as_deref());
            }
        }
    }
    out
}

/// Emit each type as a separate string fragment (for splitting).
pub fn emit_fragments(parsed: &ParsedSpec) -> Vec<String> {
    parsed
        .types
        .iter()
        .map(|td| {
            let mut buf = String::new();
            match td {
                TypeDef::Struct { name, fields, doc } => {
                    emit_struct(&mut buf, name, fields, doc.as_deref());
                }
                TypeDef::Enum {
                    name,
                    variants,
                    doc,
                } => {
                    emit_enum(&mut buf, name, variants, doc.as_deref());
                }
            }
            buf
        })
        .collect()
}

fn emit_struct(out: &mut String, name: &str, fields: &[Field], doc: Option<&str>) {
    if let Some(d) = doc {
        for line in d.lines() {
            out.push_str(&format!("/// {line}\n"));
        }
    }
    out.push_str("#[derive(Debug, Clone, Serialize, Deserialize)]\n");
    out.push_str(&format!("pub struct {name} {{\n"));

    for f in fields {
        if let Some(d) = &f.doc {
            let oneline = d.lines().next().unwrap_or("");
            if !oneline.is_empty() {
                out.push_str(&format!("    /// {oneline}\n"));
            }
        }

        // Handle serde(flatten) fields from allOf
        if f.rust_type.starts_with("#[serde(flatten)]") {
            let inner = f.rust_type.trim_start_matches("#[serde(flatten)] ");
            out.push_str("    #[serde(flatten)]\n");
            out.push_str(&format!("    pub {}: {inner},\n", sanitize_field(&f.name)));
        } else if f.required {
            out.push_str(&format!(
                "    pub {}: {},\n",
                sanitize_field(&f.name),
                f.rust_type
            ));
        } else {
            // Optional fields skip None on serialize
            out.push_str("    #[serde(skip_serializing_if = \"Option::is_none\")]\n");
            let rtype = if f.rust_type.starts_with("Option<") {
                f.rust_type.clone()
            } else {
                format!("Option<{}>", f.rust_type)
            };
            out.push_str(&format!(
                "    pub {}: {rtype},\n",
                sanitize_field(&f.name)
            ));
        }
    }

    out.push_str("}\n\n");
}

fn emit_enum(out: &mut String, name: &str, variants: &[String], doc: Option<&str>) {
    if let Some(d) = doc {
        for line in d.lines() {
            out.push_str(&format!("/// {line}\n"));
        }
    }
    out.push_str("#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]\n");
    out.push_str(&format!("pub enum {name} {{\n"));

    for v in variants {
        let pascal = to_pascal_case(v);
        if pascal != *v {
            out.push_str(&format!("    #[serde(rename = \"{v}\")]\n"));
        }
        out.push_str(&format!("    {pascal},\n"));
    }

    out.push_str("}\n\n");
}

fn to_pascal_case(s: &str) -> String {
    s.split('_')
        .map(|w| {
            let mut c = w.chars();
            match c.next() {
                None => String::new(),
                Some(f) => f.to_uppercase().to_string() + c.as_str(),
            }
        })
        .collect()
}

fn sanitize_field(name: &str) -> String {
    let s = name.replace('-', "_");
    match s.as_str() {
        "type" | "move" | "ref" | "self" | "super" | "crate" | "mod" | "fn" | "let" | "mut"
        | "pub" | "use" | "as" | "in" | "for" | "if" | "else" | "loop" | "while" | "match"
        | "return" | "break" | "continue" | "struct" | "enum" | "impl" | "trait" | "where"
        | "async" | "await" | "dyn" | "static" | "const" | "extern" | "unsafe" => {
            format!("r#{s}")
        }
        _ => s,
    }
}

const PAGINATION_TYPES: &str = r#"/// Pagination metadata for list endpoints.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PaginationMeta {
    pub page: i64,
    pub page_size: i64,
    pub total_items: i64,
    pub total_pages: i64,
}

/// Generic paginated response envelope.
///
/// Local definition without `ToSchema` bound — client crates don't need
/// OpenAPI schema derivation. Wire-compatible with `platform_http_contracts::PaginatedResponse`.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PaginatedResponse<T> {
    pub data: Vec<T>,
    pub pagination: PaginationMeta,
}

/// Simple wrapper for sub-collection list endpoints.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct DataResponse<T> {
    pub data: Vec<T>,
}

"#;
